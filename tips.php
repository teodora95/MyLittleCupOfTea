                                <h2>3. Puneti condimente in mancare</h2>         
                            <p>Mirodeniile ofera un gust placut mancarurilor, dar si o serie de "adaosuri" nutritive. De pilda,usturoiul, ceapa si ardeiul iute imbunatatesc considerabil circulatia sanguina si respiratia. Scortisoara este unul dintre cele mai vechi condimente si are proprietati antiseptice, vermifuge (combate viermii intestinali), relaxeaza, linisteste crampele (antispasmodica), indeparteaza oboseala fizica, stresul si aduce buna dispozitie. Bobitele negre de piper, cu miros patrunzator, picant, sunt fructele unui arbust catarator, originar din India. Intregi sau macinate, aceste bobite au un efect antibacterian, carminativ (elimina balonarea), stimuleaza metabolismul si ajuta la arderea grasimilor. Nucsoara constituie fructul oleaginos al unei specii de nuc verde originar din Indonezia. Cu un gust picant, usor amarui, se foloseste pentru a condimenta supe sau sosuri, diverse preparate din carne, pui, peste, legume, dar si prajituri, budinci si alte dulciuri. Este recunoscut pentru efectul sau calmant asupra sistemului nervos </p>
